class AsciiArtTool extends Tool {
  constructor() {
    super('ascii_art', {
      description: 'Convert text to ASCII art (banner style)',
      parameters: {
        type: 'object',
        properties: {
          text: { type: 'string', description: 'Text to convert to ASCII art' },
          font: { type: 'string', enum: ['simple', 'block', 'bubble'], description: 'Font style' },
        },
        required: ['text'],
      },
    });
  }

  validate(p) {
    if (!p.text || typeof p.text !== 'string') throw new Error('text is required');
    return true;
  }

  execute(p) {
    try {
      this.validate(p);
      const font = FONTS[p.font] || FONTS.simple;
      const chars = p.text.toUpperCase().split('');
      const lines = ['', '', '', '', ''];
      for (const ch of chars) {
        const glyph = font[ch] || font[' '];
        if (glyph) {
          for (let i = 0; i < 5; i++) {
            lines[i] += glyph[i] + ' ';
          }
        }
      }
      const art = lines.join('\n');
      return this.formatResult({ art, font: p.font || 'simple', text: p.text, lines: 5, width: lines[0].length });
    } catch (e) {
      logger.error(`AsciiArtTool failed: ${e.message}`);
      return this.formatError(e);
    }
  }
}

module.exports = AsciiArtTool;
